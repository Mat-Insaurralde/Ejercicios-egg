
package EX1;


public class Cancion {

  //ATRIBUTOS
    
    public String autor;
    public String titulo;
    
    
 ///CONTRU   

    public Cancion() {
    }

    public Cancion(String autor, String titulo) {
        this.autor = autor;
        this.titulo = titulo;
    }
    
   /// 

//GETTERS AND SETTERs

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }











    
}
